    .global _delay_wait  /*! global delay wait routine */
    .short  _delay_wait

;-----------------------------------------------------------------------------
;    delay_wait
;-----------------------------------------------------------------------------
_delay_wait:
    ;--------------------------------------------------
    ; wait loop for low word
    ;--------------------------------------------------
loop:
    SUBW    AX, #1
    BZ      $check
    BR      $loop

    ;--------------------------------------------------
    ; check loop for high word carried
    ;--------------------------------------------------
check:
    CMPW    AX, BC
    DECW    BC
    BNZ     $loop

    ;--------------------------------------------------
    ; setting the stack pointer
    ;--------------------------------------------------

_exit:
    RET
